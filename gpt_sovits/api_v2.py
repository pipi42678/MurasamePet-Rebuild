def check_params(req: dict):
    text: str = req.get("text", "")
    text_lang: str = req.get("text_lang", "")
    ref_audio_path: str = req.get("ref_audio_path", "")
    streaming_mode: bool = req.get("streaming_mode", False)
    media_type: str = req.get("media_type", "wav")
    prompt_lang: str = req.get("prompt_lang", "")
    text_split_method: str = req.get("text_split_method", "cut5")

    if ref_audio_path in [None, ""]:
        return JSONResponse(status_code=400, content={"message": "ref_audio_path is required"})
    if text in [None, ""]:
        return JSONResponse(status_code=400, content={"message": "text is required"})
    if text_lang in [None, ""]:
        return JSONResponse(status_code=400, content={"message": "text_lang is required"})
    elif text_lang.lower() not in tts_config.languages:
        return JSONResponse(
            status_code=400,
            content={"message": f"text_lang: {text_lang} is not supported in version {tts_config.version}"},
        )
    if prompt_lang in [None, ""]:
        return JSONResponse(status_code=400, content={"message": "prompt_lang is required"})
    elif prompt_lang.lower() not in tts_config.languages:
        return JSONResponse(
            status_code=400,
            content={"message": f"prompt_lang: {prompt_lang} is not supported in version {tts_config.version}"},
        )
    if media_type not in ["wav", "raw", "ogg", "aac"]:
        return JSONResponse(status_code=400, content={"message": f"media_type: {media_type} is not supported"})
    elif media_type == "ogg" and not streaming_mode:
        return JSONResponse(status_code=400, content={"message": "ogg format is not supported in non-streaming mode"})

    if text_split_method not in cut_method_names:
        return JSONResponse(
            status_code=400, content={"message": f"text_split_method:{text_split_method} is not supported"}
        )

    return None


async def tts_handle(req: dict):
    """
    Text to speech handler.
    """
    logger.info(f"🔍 开始处理TTS请求，参数: { {k: v for k, v in req.items() if k not in ['ref_audio_path']} }")  # 不记录完整路径

    # 处理种子值
    seed = req.get("seed", -1)
    if seed != -1:
        MAX_INT32 = 2147483647
        if seed > MAX_INT32:
            original_seed = seed
            seed = seed % (MAX_INT32 + 1)
            logger.warning(f"⚠️ 种子值超出范围: {original_seed} -> {seed}")
            req["seed"] = seed

    streaming_mode = req.get("streaming_mode", False)
    return_fragment = req.get("return_fragment", False)
    media_type = req.get("media_type", "wav")

    logger.debug(f"参数检查: streaming_mode={streaming_mode}, media_type={media_type}")

    check_res = check_params(req)
    if check_res is not None:
        logger.error(f"❌ 参数检查失败: {check_res.body}")
        return check_res

    if streaming_mode or return_fragment:
        req["return_fragment"] = True
        logger.debug("启用片段返回模式")

    try:
        logger.info("🎯 开始调用TTS管道...")
        tts_generator = tts_pipeline.run(req)
        logger.info("✅ TTS管道调用成功")

        if streaming_mode:
            logger.debug("📡 流式输出模式")

            def streaming_generator(tts_generator: Generator, media_type: str):
                if_frist_chunk = True
                chunk_count = 0
                for sr, chunk in tts_generator:
                    if if_frist_chunk and media_type == "wav":
                        logger.debug(f"🎵 生成WAV头部，采样率: {sr}")
                        yield wave_header_chunk(sample_rate=sr)
                        media_type = "raw"
                        if_frist_chunk = False
                    chunk_count += 1
                    logger.debug(f"📦 生成音频片段 #{chunk_count}, 大小: {len(chunk)}")
                    yield pack_audio(BytesIO(), chunk, sr, media_type).getvalue()
                logger.info(f"✅ 音频生成完成，共 {chunk_count} 个片段")

            return StreamingResponse(
                streaming_generator(tts_generator, media_type),
                media_type=f"audio/{media_type}",
            )

        else:
            logger.debug("📦 非流式输出模式")
            sr, audio_data = next(tts_generator)
            logger.info(f"✅ 音频生成成功，采样率: {sr}, 数据大小: {len(audio_data)}")
            audio_data = pack_audio(BytesIO(), audio_data, sr, media_type).getvalue()
            return Response(audio_data, media_type=f"audio/{media_type}")

    except Exception as e:
        # 记录完整的错误信息
        error_detail = traceback.format_exc()
        logger.error(f"❌ TTS处理失败: {str(e)}")
        logger.error(f"📋 错误详情:\n{error_detail}")

        # 提供详细的错误响应
        error_info = {
            "message": "语音合成失败",
            "error": str(e),
            "error_type": e.__class__.__name__,
            "request_info": {
                "text_length": len(req.get("text", "")) if req.get("text") else 0,
                "text_lang": req.get("text_lang"),
                "prompt_lang": req.get("prompt_lang"),
                "seed": req.get("seed"),
                "batch_size": req.get("batch_size"),
                "streaming_mode": req.get("streaming_mode"),
                "media_type": req.get("media_type")
            }
        }

        # 如果是种子相关错误
        if "seed" in str(e).lower() or "2971199551" in str(e):
            error_info["suggestion"] = "种子值可能超出有效范围(0-2147483647)，请使用-1（随机）或有效的种子值"

        return JSONResponse(
            status_code=400,
            content=error_info
        )


